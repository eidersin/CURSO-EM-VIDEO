'''Tipos Primitivos e Saída de Dados.

Tipos Primitivos
 - INT = 7, -4, 0, 9875
 - FLOAT = 4.5, 0.076, -15.223
 - BOOL = True or False
 - STR = 'Hello World!'

 Operadores Aritméticos
num1 = eval(input("Digite o primeiro número: "))
num2 = eval(input("Digite o segundo número: "))
 + = Adição
soma = num1 + num2
print('A soma é: {}'.format(soma))
 - = Subtração
sub = num1 - num2
print('A subtração é: {}'.format(sub))
 * = Multriplicação
mul = num1 * num2
print('A multiplicacao é: {}'.format(mul))
 / = Divisão
div = num1 / num2
print('A divisão é: {:.2f}'.format(div))
 ** = Potência
pot = num1 ** num2
print('A potencia é: {}'.format(pot))
 // = Divisão Inteira
divInteira = num1 // num2
print('A divisão inteira é: {}'.format(divInteira))
 % = Resto da Divisão
sobra = num1 % num2
print('O resto da divisão é: {}'.format(sobra))

 Ordem de precedência
 1 - ()
 2 - **
 3 - *, /, //, %
 4 - + ou -
'''