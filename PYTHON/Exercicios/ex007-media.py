
# Exercício 7 – Média Aritmética

print('====== Exercício 7 – Média Aritmética')
nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))
print('A sua primeira nota foi {:.1f} e sua segunda nota foi {:.1f} '
      'sendo sua média final {:.1f}'.format(nota1, nota2, (nota1 + nota2) / 2 ))
